// LED stuff below

#define LED_MAX 64
#define USE_BRIGHTNESS 1

uint8_t LED_Data[LED_MAX][4];
uint8_t LED_Mod[LED_MAX][4]; // brightness adjusted

int datasentflag = 0;

void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim) {
	HAL_TIM_PWM_Stop_DMA(&htim1, TIM_CHANNEL_1);
	datasentflag = 1;
}

void Set_LED(int LED_num, int red, int green, int blue) {
	LED_Data[LED_num][0] = LED_num;
	LED_Data[LED_num][1] = green;
	LED_Data[LED_num][2] = red;
	LED_Data[LED_num][3] = blue;
}

#define PI 3.14159265

void Set_Brightness(int brightness) {
#if USE_BRIGHTNESS
	if (brightness > 45) brightness = 45; // Max brightness
	for (int i = 0; i < LED_MAX; i++) {
		LED_Mod[i][0] = LED_Data[i][0];
		for (int j = 1; j < 4; j++) {
			float angle = 90-brightness;
			angle = angle*PI/180;
			LED_Mod[i][j] = (LED_Data[i][j])/(tan(angle));
		}
	}
#endif
}

uint16_t pwmData[(24*LED_MAX)+50];

void send() {

	uint32_t idx = 0;
	uint32_t color;
	for (int i = 0; i < LED_MAX; i++) {
		color = ((LED_Mod[i][1]<<16) | (LED_Mod[i][2]<<8) | (LED_Mod[i][3]));
		for (int i = 23; i >= 0; i--) {
			if (color&(1<<i)) {
				pwmData[idx] = 60;
			}
			else {
				pwmData[idx] = 30;
			}
			idx++;
		}
	}
	//Reset here (50 us)
	for (int i = 0; i < 50; i++) {
		pwmData[idx] = 0;
		idx++;
	}

	HAL_TIM_PWM_Start_DMA(&htim1, TIM_CHANNEL_1, (uint32_t *)pwmData, idx);
	while (!datasentflag) {};
	datasentflag = 0;
}

void fade() {

	for (int i = 0; i >= 10; i++) {
		Set_Brightness(i);
		send();
		HAL_Delay(25);
	}
	for (int i = 10; i >= 0; i--) {
		Set_Brightness(i);
		send();
		HAL_Delay(25);
	}
}

